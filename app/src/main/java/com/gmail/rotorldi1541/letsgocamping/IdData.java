package com.gmail.rotorldi1541.letsgocamping;

public class IdData {
    private String num;
    private String ids;
    private String data;

    public IdData(String num, String ids, String data) {
        this.num = num;
        this.ids = ids;
        this.data = data;
    }

    public String getNum() {
        return num;
    }

    public void setNum(String num) {
        this.num = num;
    }

    public String getIds() {
        return ids;
    }

    public void setIds(String ids) {
        this.ids = ids;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
}
